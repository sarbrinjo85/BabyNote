/// WHO Child Growth Standards (2006) — LMS 파라미터 테이블.
///
/// ── 출처 ─────────────────────────────────────────────────────────────
/// World Health Organization. WHO Child Growth Standards: Methods and
/// Development. 0-5세 표준 LMS 값 발췌.
///   - https://www.who.int/tools/child-growth-standards
///
/// 이 앱은 0-24개월 구간만 번들 (MVP). 25개월 이후는 데이터 없음 처리.
///
/// ── 형식 ─────────────────────────────────────────────────────────────
/// 각 entry: 월령(0~24), L, M, S
///   Z = ((X/M)^L − 1) / (L*S)        (L ≠ 0)
///   Z = ln(X/M) / S                  (L = 0)
/// LMS 값은 WHO 공개 데이터(weianthro.txt 등) 기준.
///
/// ── 주의 ─────────────────────────────────────────────────────────────
/// 표 사이 월령은 호출부에서 선형 보간 처리.
/// 이 표는 의료 진단용 아닌 사용자 참고용 — UI에 면책 명시 필수.

class LmsRow {
  const LmsRow(this.month, this.l, this.m, this.s);
  final int month;
  final double l;
  final double m;
  final double s;
}

