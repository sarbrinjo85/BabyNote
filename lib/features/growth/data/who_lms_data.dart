enum WhoMetric { weight, height, headCirc }

List<LmsRow> whoTable(WhoMetric metric, {required bool isMale}) {
  switch (metric) {
    case WhoMetric.weight:
      return isMale ? kWeightBoys : kWeightGirls;
    case WhoMetric.height:
      return isMale ? kHeightBoys : kHeightGirls;
    case WhoMetric.headCirc:
      return isMale ? kHcBoys : kHcGirls;
  }
}
