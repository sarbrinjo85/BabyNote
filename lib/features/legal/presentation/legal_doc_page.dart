///
/// docs/*.md의 텍스트 내용을 인앱 ListView로 렌더 (외부 링크 열지 않음).
/// 약관 내용은 docs/privacy_policy.md / terms_of_service.md 와 동기화 유지.
class LegalDocPage extends StatelessWidget {
  const LegalDocPage._({required this.title, required this.content});

  factory LegalDocPage.privacy() =>
      const LegalDocPage._(title: '개인정보처리방침', content: _privacy);
  factory LegalDocPage.terms() =>
      const LegalDocPage._(title: '이용약관', content: _terms);

  final String title;
  final String content;

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(title),
        actions: [
          IconButton(
            tooltip: '복사',
            icon: const Icon(Icons.copy),
            onPressed: () async {
              await Clipboard.setData(ClipboardData(text: content));
              if (!context.mounted) return;
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(content: Text('전체 내용을 복사했어요')),
              );
            },
          ),
        ],
      ),
      body: SafeArea(
        top: false,
        child: ListView(
          padding: const EdgeInsets.all(Spacing.md),
          children: [
            SelectableText(
              content,
              style: const TextStyle(
                fontSize: 14,
                height: 1.6,
              ),
            ),
            const SizedBox(height: Spacing.xl),
          ],
        ),
      ),
    );
  }
}

