-- =============================================================================
-- 12_diaper_amount.sql — diapers에 amount(대변 양) 컬럼 추가
-- =============================================================================
--
-- consistency(묽음/보통/단단함)는 형태/질감, amount(조금/보통/많음)는 분량.
-- 둘 다 대변 또는 '둘다' 케이스에서만 의미. 소변에는 보통 비어 있음.
-- =============================================================================

alter table public.diapers
  add column if not exists amount text
  check (amount is null or amount in ('small','normal','large'));
