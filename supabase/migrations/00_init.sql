-- =============================================================================
-- 00_init.sql — 확장(extension) + 공통 트리거 함수
-- =============================================================================
--
-- 이 파일은 가장 먼저 실행되어야 함. 다른 마이그레이션이 여기서 정의한
-- set_updated_at() 함수를 트리거에서 사용하기 때문.
--
-- ※ is_caregiver_of() 헬퍼는 caregivers 테이블에 의존하므로 01 파일 끝에 정의함.
--   (`language sql` 함수는 생성 시점에 본문의 테이블 참조를 즉시 검증하므로
--    의존하는 객체보다 먼저 만들 수 없음. 의존성 순서대로 파일을 분리한 이유.)
-- =============================================================================

-- pgcrypto: gen_random_uuid()를 제공. uuid를 PK로 쓰려면 필수.
-- (Supabase에는 보통 미리 활성화되어 있지만 안전하게 명시.)
create extension if not exists pgcrypto;


-- ─────────────────────────────────────────────────────────────────────────────
-- 공통 트리거 함수: updated_at 자동 갱신
-- ─────────────────────────────────────────────────────────────────────────────
-- UPDATE가 발생할 때마다 NEW.updated_at = now() 로 강제.
-- 이걸 모든 테이블에 트리거로 붙이면 클라이언트가 잊어버려도 DB가 알아서 채움.
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;
